# 容器类型数据转换

# list 列表
'''
    数字类型是非容器类型 不能转化为列表
    字符串转化为列表时 会把字符串中的每一个字符当做列表的元素
    集合 可以转化为 list列表类型
    元组 可以转化为 list列表类型
    字典 可以转化为 list列表类型 但是只保留字典中的键
'''
